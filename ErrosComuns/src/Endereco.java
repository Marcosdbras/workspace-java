
public class Endereco {
   private String tipoLogrador;
   private String logrador;
   private String cidade;
   private String estado;
public String getTipoLogrador() {
	return tipoLogrador;
}
public void setTipoLogrador(String tipoLogrador) {
	this.tipoLogrador = tipoLogrador;
}
public String getLogrador() {
	return logrador;
}
public void setLogrador(String logrador) {
	this.logrador = logrador;
}
public String getCidade() {
	return cidade;
}
public void setCidade(String cidade) {
	this.cidade = cidade;
}
public String getEstado() {
	return estado;
}
public void setEstado(String estado) {
	this.estado = estado;
}
}
