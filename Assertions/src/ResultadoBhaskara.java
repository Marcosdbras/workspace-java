
public class ResultadoBhaskara {
    
	/*
	 * Este classe foi criada pois um método só retorna uma valor
	 * por vez então criou-se esta classe para acomodar as raizes 1 e 2
	 * para posteriormente exibir o resultado do cálculo
	 * 
	 * */
	
	private double r1;
	private double r2;

	public void setValores(double r1, double r2) {
		
		this.r1 = r1;
		this.r2 = r2;
		
	}

	public double getR1() {
		return r1;
	}

	public double getR2() {
		return r2;
	}
	
	

	
	

}
