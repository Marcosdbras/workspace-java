
public class GenericoException extends Exception {

	public GenericoException() {
		
	}

	public GenericoException(String message) {
		super(message);
		
	}

	public GenericoException(Throwable cause) {
		super(cause);
		
	}

	public GenericoException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public GenericoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
